export function translate(language: AppLanguage, key: I18nKey) {
  if (language === "vi") {
    return viCleanOverrides[key] ?? viOverrides[key] ?? dictionary.en[key] ?? key;
  }
  return dictionary[language][key] ?? dictionary.en[key] ?? key;
}

export function useI18n() {
  const language = usePreferencesStore((s) => s.language);
  const loaded = usePreferencesStore((s) => s.loaded);
  const loadLanguage = usePreferencesStore((s) => s.loadLanguage);

  useEffect(() => {
    if (!loaded) void loadLanguage();
  }, [loadLanguage, loaded]);

  return {
    language,
    t: (key: I18nKey) => translate(language, key),
  };
}
